% Page307：已知二阶线性系统的微分方程，绘制系统的相平面图和零输入响应曲线
clear;
clc;

% Eg11_2_test 是系统微分方程的描述函数
% 初始化状态变量为 [0,1]，计算时间为 [0,20]
[t, x] = ode45('Eg11_2_test', [0,20], [0,1]);
% 初始化状态变量为 [1,1]，计算时间为 [0,20]
[t1, x1] = ode45('Eg11_2_test', [0,20], [1,1]);
% 绘制相轨迹
plot(x(:,1),x(:,2),'-', x1(:,1),x1(:,2),'.')
grid
% 添加图标题
title('相轨迹图')

% 绘制时域响应曲线
plot(t,x(:,2),'-', t1,x1(:,2),'.')
% 添加图标题
title('时间响应曲线')
